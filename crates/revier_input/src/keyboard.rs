    pub fn change_key(&mut self,keycode:u32){
        *self.keys.get_mut(&keycode).unwrap() = true;
        *self.keys_changed.get_mut(&keycode).unwrap() = true;
    }

    pub fn get_key(&self,keycode:Keycode) -> bool{
        return *self.keys.get(&(keycode as u32)).unwrap();
    }

    pub fn keys_changed(&mut self,keycode:Keycode) -> bool{
        let number = &(keycode as u32);

        let result:bool = if *self.keys_changed.get(number).unwrap(){
            true
        }else{
            false
        };
        *self.keys_changed.get_mut(number).unwrap() = false;
        
        return result;
    }

    pub fn key_went_up(&mut self,keycode:Keycode) -> bool{
         let number = &(keycode as u32);   

        return !*self.keys.get(number).unwrap() && self.keys_changed(self.from_u32(number).unwrap());
    }

    pub fn key_went_down(&mut self,keycode:Keycode) -> bool{
        let number = &(keycode as u32);   

        return *self.keys.get(number).unwrap() && self.keys_changed(self.from_u32(number).unwrap());
    }

    pub fn reset_keys(&mut self){
        for (_,value) in &mut self.keys_changed{
            *value = false;
        }

        for (_,value) in &mut self.keys{
            *value = false;
        }
    }

